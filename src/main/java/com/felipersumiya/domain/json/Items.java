package com.felipersumiya.domain.json;

public class Items {
	
	private String name;
	private String role;
	
	public Items() {
		
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getRole() {
		return role;
	}

	public void setRole(String role) {
		this.role = role;
	}
	

}
