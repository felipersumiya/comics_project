package com.felipersumiya.domain.json;

public class Prices {
	
	private String type;
	private Double price;
	
	public Prices() {
		
	}

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}


	public Double getPrice() {
		return price;
	}

	public void setPrice(Double price) {
		this.price = price;
	}

}
